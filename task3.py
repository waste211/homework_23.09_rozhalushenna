# Дано дві змінні цілого типу: A і B. Якщо їх значення не рівні, то присвоїти кожній змінній більше з цих значень,
# а якщо рівні, то присвоїти змінним нульові значення. Вивести нові значення змінних A і B.

a, b = map(int, input("Введіть А, В: ").split())

if a != b:
    if a > b:
        b = a
    if b > a:
        a = b
elif a == b:
    a = 0
    b = 0

print("A = " + str(a) + ", B = " + str(b))

