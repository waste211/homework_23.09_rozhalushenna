# Дано три цілих числа: A, B, C. Перевірити істинність висловлювання: «Рівно два з чисел A, B, C є позитивними».

a, b, c = map(int, input("Введіть А, В, С: ").split())
ch = 0

if a > 0:
    ch+=1
if b > 0:
    ch+=1
if c > 0:
    ch+=1

if ch == 2:
    print("Рівно два з чисел A, B, C є позитивними")
